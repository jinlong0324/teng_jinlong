#include <iostream>

using namespace std;

int main()
{
    int a,min,b;
    int n=1;
    cout<<"Enter first integer";
    cin>>a;
    for(n;n<=n;++n)
    {
        a>>b;
        if(n==1)
        {
            min=a;
        }
        if(min>b)
        {
            min=b;
        }
    }

    cout<<"Min is"<<min<<endl;
    return 0;
}
